    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        InputStream doc = null;
                try {
                    Connection cnx = db.connexionDatabase();
                    doc = new FileInputStream(new File(s));
                    String sql ="update documentation set file=? where codedoc='"+txt_codedoc.getText()+"'";
                    PreparedStatement prepared = cnx.prepareStatement(sql);
                    prepared.setBlob(1,doc);
                    prepared.execute();
                } catch (FileNotFoundException | SQLException ex) {
                    Logger.getLogger(listedocumentation.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    try {
                        doc.close();
                    } catch (IOException ex) {
                        Logger.getLogger(listedocumentation.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
       
			
    }//GEN-LAST:event_jButton10ActionPerformed

