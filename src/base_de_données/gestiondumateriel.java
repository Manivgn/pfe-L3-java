    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       if (txt_rech.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "SVP entrer quelque chose");
        } else {
            if (com_rech.getSelectedItem().equals("idmateriel")) {
                rs = db.querySelectAll("materiel", "idmateriel LIKE '%" + txt_rech.getText() + "%' ");
                table_materiel.setModel(new ResultSetTableModel(rs));
            } else if (com_rech.getSelectedItem().equals("numsalle")) {
                rs = db.querySelectAll("materiel", "numsalle LIKE '%" + txt_rech.getText() + "%' ");
                table_materiel.setModel(new ResultSetTableModel(rs));
            } else if (com_rech.getSelectedItem().equals("etatmateriel")) {
                rs = db.querySelectAll("materiel", "etatmateriel LIKE '%" + txt_rech.getText() + "%' ");
                table_materiel.setModel(new ResultSetTableModel(rs));
            } else if (com_rech.getSelectedItem().equals("typetypemateriel")) {
                rs = db.querySelectAll("materiel", "typemateriel LIKE '%" + txt_rech.getText() + "%' ");
                table_materiel.setModel(new ResultSetTableModel(rs));
            } else if (com_rech.getSelectedItem().equals("numcmd")) {
                rs = db.querySelectAll("materiel", "numcmd LIKE '%" + txt_rech.getText() + "%' ");
                table_materiel.setModel(new ResultSetTableModel(rs));
            }else if (com_rech.getSelectedItem().equals("departement")) {
                String a[] ={"idmateriel","numsalle","dateacquisition","valeuracquisition","etatmateriel","modelemateriel","codefamille","garantie","typemateriel","numcmd"};
                rs = db.fcSelectCommand(a, "materiel natural join salle natural join departement", "intituledept LIKE '%" + txt_rech.getText() + "%' ") ;
                table_materiel.setModel(new ResultSetTableModel(rs));
            }
        } 
    }//GEN-LAST:event_jButton5ActionPerformed

