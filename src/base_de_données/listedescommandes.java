    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
            if (txt_datefacturation.getText().equals("") || txt_fichier.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "SVP veuillez remplir tous les champs");
        } else {
                InputStream doc = null;
            
                try {
                    Connection cnx = db.connexionDatabase();
                    doc = new FileInputStream(new File(s));
                    String sql ="update commande set datefacturation=? , facture=? where numcmd='"+ num +"'";
                    PreparedStatement prepared = cnx.prepareStatement(sql);
                    prepared.setString(1,txt_datefacturation.getText());
                    prepared.setBlob(2,doc);  
                    prepared.execute();
                } catch (FileNotFoundException | SQLException ex) {
                    java.util.logging.Logger.getLogger(listedocumentation.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                } finally {
                    try {
                        doc.close();
                    } catch (IOException ex) {
                        java.util.logging.Logger.getLogger(listedocumentation.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                    }
                }
                
       
            }
    }//GEN-LAST:event_jButton10ActionPerformed

